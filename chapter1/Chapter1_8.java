package chapter1_8;

public class Chapter1_8 {

    public static void main(String[] args) {
//        DECLARE 
        double perimeter, area;
        double radius = 5.5;
        double phi = 3.14;
//        PERIMETER OF CIRCLE
        perimeter = 2 * radius * phi;
//        AREA OF CIRCLE
        area = radius * radius * phi;
//        OUTPUT
        System.out.println("perimeter   : " + perimeter);
        System.out.println("area        : " + area);

    }

}
